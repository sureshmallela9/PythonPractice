def calculateAreaOfRectangular(length, width):
    return length * width

print(calculateAreaOfRectangular(4, 5))

#The area of a rectangle is calculated using this simple formula:
# Area = Length * Width // result with be squre units

#  What “Square Units” Means
# When we calculate area, we’re measuring how much space a shape covers on a flat surface.
# Since area is length × width, the units also multiply:
# - If length and width are measured in meters, the area is in square meters (m²).
# - If measured in centimeters, the area is in square centimeters (cm²).
# - If measured in feet, the area is in square feet (ft²).



